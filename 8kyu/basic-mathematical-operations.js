function basicOp(operation, v1, v2)
{
  // Loop if operation is +-*/ do corresponding actoin
  if( operation == "+") {
    return v1 + v2
  }
  if( operation == "-") {
    return v1 - v2
  }
  if( operation == "*") {
    return v1 * v2
  }
  if( operation == "/") {
    return v1 / v2
  }
}